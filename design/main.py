import math


def main() -> None:
    """The main() function calculates the circumference of a 42 mm circle,returns None."""
    print("If a circle has a diameter of 42 mm:\n")
    print(f"Circumference is {(math.pi * 42):.5f} mm.")

    print("\nDone.")


if __name__ == "__main__":
    main()
